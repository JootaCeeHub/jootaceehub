export function getSkillsByCategory(category: SkillCategory): SkillEntry[] {
  return SKILL_LIBRARY.filter(s => s.category === category)
}

export function searchSkills(query: string): SkillEntry[] {
  const q = query.toLowerCase()
  return SKILL_LIBRARY.filter(s =>
    s.name.toLowerCase().includes(q) ||
    s.description.toLowerCase().includes(q) ||
    s.tags.some(t => t.includes(q)) ||
    s.capabilities.some(c => c.toLowerCase().includes(q)) ||
    s.useCases.some(u => u.toLowerCase().includes(q))
  )
}

export function getSkillById(id: string): SkillEntry | undefined {
  return SKILL_LIBRARY.find(s => s.id === id)
}

